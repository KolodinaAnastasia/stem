#' Stemmer Porter for Russia
#' @export
#' @param word string
stem <- function(word) {
  # PERFECTIVEGROUND
  word <- gsub("(\\u0438\\u0432|\\u0438\\u0432\\u0448\\u0438|\\u0438\\u0432\\u0448\\u0438\\u0441\\u044c|\\u044b\\u0432|\\u044b\\u0432\\u0448\\u0438|\\u044b\\u0432\\u0448\\u0438\\u0441\\u044c|[\\u0430\\u044f]\\u0432|[\\u0430\\u044f]\\u0432\\u0448\\u0438|[\\u0430\\u044f]\\u0432\\u0448\\u0438\\u0441\\u044c)$", "", word, perl=TRUE)
  # REFLEXIVE
  word <- gsub("(\\u0441[\\u044f\\u044c])$", "", word, perl=TRUE)
  # ADJECTIVE
  word <- gsub("(\\u0435\\u0435|\\u0438\\u0435|\\u044b\\u0435|\\u043e\\u0435|\\u0438\\u043c\\u0438|\\u044b\\u043c\\u0438|\\u0435\\u0439|\\u0438\\u0439|\\u044b\\u0439|\\u043e\\u0439|\\u0435\\u043c|\\u0438\\u043c|\\u044b\\u043c|\\u043e\\u043c|\\u0435\\u0433\\u043e|\\u043e\\u0433\\u043e|\\u0435\\u043c\\u0443|\\u043e\\u043c\\u0443|\\u0438\\u0445|\\u044b\\u0445|\\u0443\\u044e|\\u044e\\u044e|\\u0430\\u044f|\\u044f\\u044f|\\u043e\\u044e|\\u0435\\u044e)$", "", word, perl=TRUE)
  # PARTICIPLE
  word <- gsub("(\\u0438\\u0432\\u0448|\\u044b\\u0432\\u0448|\\u0443\\u044e\\u0449|[\\u0430\\u044f]\\u044e\\u0449|[\\u0430\\u044f]\\u0449)$", "", word, perl=TRUE)
  # VERB
  word <- gsub("(\\u0438\\u043b\\u0430|\\u044b\\u043b\\u0430|\\u0435\\u043d\\u0430|\\u0435\\u0439\\u0442\\u0435|\\u0443\\u0439\\u0442\\u0435|\\u0438\\u0442\\u0435|\\u0438\\u043b\\u0438|\\u044b\\u043b\\a|\\u0435\\u043d\\u0438|\\u0438\\u043c|\\u044b\\u043c|\\u0435\\u043d|\\u0438\\u043b\\u043e|\\u044b\\u043b\\u043e|\\u0435\\u043d\\u043e|\\u044f\\u0442|\\u0443\\u0435\\u0442|\\u0443\\u044e\\u0442|\\u0438\\u0442|\\u044b\\u0442|\\u0435\\u043d\\u044b|\\u0438\\u0442\\u044c|\\u044b\\u0442\\u044c|\\u0438\\u0448\\u044c|\\u0443\\u044e|\\u044e|[\\u0430\\u044f]\\u0442\\u0435|[\\u0430\\u044f]\\u044e\\u0442|[\\u0430\\u044f]\\u0442)$", "", word, perl=TRUE)
  # NOUN
  word <- gsub("(\\u0430|\\u0435\\u0432|\\u043e\\u0432|\\u0438\\u0435|\\u044c\\u0435|\\u0435|\\u0438\\u044f\\u043c\\u0438|\\u044f\\u043c\\u0438|\\u0430\\u043c\\u0438|\\u0435\\u0438|\\u0438\\u0438|\\u0438|\\u0438\\u0435\\u0439|\\u0435\\u0439|\\u043e\\u0439|\\u0438\\u0439|\\u0439|\\u0438\\u044f\\u043c|\\u044f\\u043c|\\u0438\\u0435\\u043c|\\u0435\\u043c|\\u0430\\u043c|\\u043e\\u043c|\\u043e|\\u0443|\\u0430\\u0445|\\u0438\\u044f\\u0445|\\u044f\\u0445|\\u044b|\\u044c|\\u0438\\u044e|\\u044e\\u044e|\\u044e|\\u0438\\u044f|\\u044c\\u044f|\\u044f)$", "", word, perl=TRUE)
  # DERIVATIONAL
  word <- gsub("[^\\u0430\\u0435\\u0438\\u043e\\u0443\\u044b\\u044d\\u044e\\u044f]+[\\u0430\\u0435\\u0438\\u043e\\u0443\\u044b\\u044d\\u044e\\u044f]\\u043e\\u0441\\u0442\\u044c?$", "", word, perl=TRUE)
  # DER
  word <- gsub("\\u043e\\u0441\\u0442\\u044c?$", "", word, perl=TRUE)
  # SUPERLATIVE
  word <- gsub("(\\u0435\\u0439\\u0448\\u0435|\\u0435\\u0439\\u0448)$", "", word, perl=TRUE)
  # I
  word <- gsub("\\u0438$", "", word, perl=TRUE)
  # P
  word <- gsub("\\u044c$", "", word, perl=TRUE)
  # NN
  word <- gsub("\\u043d\\u043d$", "\\u043d", word, perl=TRUE)

  return(word)
}



